package co.unicauca.microservice.apirest.exception;

/**
 * Enumeración de códigos de error.
 *
 * Esta enumeración define los códigos de error posibles que pueden ser utilizados en la aplicación.
 */
public enum EnumErrorCodes {
    /**
     * Campo vacío.
     */
    EMPTY_FIELD,

    /**
     * Número inválido.
     */
    INVALID_NUMBER,

    /**
     * Número de tarea (TODO) inválido.
     */
    INVALID_TODO_NUMBER;
}