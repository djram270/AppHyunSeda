/**
 * Enumeración de los roles de usuario.
 *
 * Esta enumeración define los roles disponibles para los usuarios en la aplicación.
 */
public enum Role {
    /**
     * Rol de administrador.
     */
    SUPER_ADMIN,

    /**
     * Rol de administrador.
     */
    ADMIN,

    /**
     * Rol de usuario.
     */
    USER
}